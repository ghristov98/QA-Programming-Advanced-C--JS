// ***** Сортиране на асоциативен масив по ключ *****

// Методът localeCompare() сравнява две низови стойности (strings) и връща число, 
// което указва сравнителния ред между тях, като отчита локализацията (език и регион).

a.localeCompare(b)

// Връща:
// -1 → ако a е преди b в речников (лексикографски) ред
// 0 → ако са еднакви
// 1 → ако a е след b

// Какво прави localeCompare в примера на седма задача?

let sorted = Object.entries(book).sort((a, b) => a[0].localeCompare(b[0]));

// 1. Object.entries(book)
//Това превръща обекта book в масив от двойки [ключ, стойност], т.е.:

[
  ['Adam', 'Some Street 1'],
  ['Zara', 'Other Street 2'],
  ['Peter', 'Another Street']
]

//2. .sort((a, b) => a[0].localeCompare(b[0]))
// Тук сортираме по имената(ключа), които се намират на индекс 0 във всяка двойка.

// Ако имаме следните двойки:

a = ['Adam', 'Street A']
b = ['Peter', 'Street P']

// a[0] е 'Adam', b[0] е 'Peter', така че:

'Adam'.localeCompare('Peter') // връща -1

// → Adam ще остане преди Peter в сортирания масив.